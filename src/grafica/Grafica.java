    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if (sensorSeleccionado == null) {
            labelResultado.setText("Seleccioná un sensor primero.");
            return;
        }

        String texto = campoValor.getText().trim();
        if (texto.isEmpty()) {
            labelResultado.setText("Ingresá un valor.");
            return;
        }

        try {
            double valor = Double.parseDouble(texto);

            if (sensorSeleccionado instanceof SensorTemperatura st) {
                st.registrarTemperatura(valor);
            } else if (sensorSeleccionado instanceof SensorNivel sn) {
                sn.registrarNivel(valor);
            } else if (sensorSeleccionado instanceof SensorPresion sp) {
                sp.registrarPresion(valor);
            } else if (sensorSeleccionado instanceof SensorCaudal sc) {
                sc.registrarCaudal(valor);
            } else if (sensorSeleccionado instanceof SensorProximidad sp) {
                sp.detectarBotella();
            }

            sistema.evaluarSistema();
            actualizarPanelActuadores();
            labelResultado.setText("Valor registrado en " + sensorSeleccionado.getNombre());

        } catch (NumberFormatException e) {
            labelResultado.setText("Ingresá un número válido.");
        } catch (excepciones.SensorDesactivadoException e) {
            labelResultado.setText("El sensor está desactivado.");
        } catch (excepciones.ValorFueraDeRangoException e) {
            labelResultado.setText("Valor fuera de rango.");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

