    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        areaHistorial.setText("");
        GestorArchivos gestor = new GestorArchivos();
        if (!gestor.existeArchivo("historial.txt")) {
            areaHistorial.setText("Sin eventos registrados.");
            return;
        }
        try {
            ArrayList<String> lineas = gestor.obtenerLineas("historial.txt");
            for (int i = lineas.size() - 1; i >= 0; i--) {
                areaHistorial.append(lineas.get(i) + "\n");
            }
        } catch (IOException e) {
            areaHistorial.setText("Error al leer historial.");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

