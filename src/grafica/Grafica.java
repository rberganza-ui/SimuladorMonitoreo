    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String tipo = (String) comboTipo.getSelectedItem();
        String nombre = campoNombre.getText().trim();

        if (nombre.isEmpty()) {
            nombre = tipo + (sensores.size() + 1);
        }

        Sensor nuevo = switch (tipo) {
            case "SensorTemperatura" -> new SensorTemperatura(nombre);
            case "SensorNivel" -> new SensorNivel(10.0, nombre);
            case "SensorPresion" -> new SensorPresion(nombre);
            case "SensorCaudal" -> new SensorCaudal(nombre);
            case "SensorProximidad" -> new SensorProximidad(nombre);
            default -> null;
        };

        if (nuevo != null) {
            sensores.add(nuevo);
            sistema.agregarSensor(nuevo);

            DefaultListModel<String> model = (DefaultListModel<String>) jList1.getModel();
            model.addElement(nuevo.getNombre());

            campoNombre.setText("");
            labelAgregarResultado.setForeground(new java.awt.Color(0, 130, 0));
            labelAgregarResultado.setText(nombre + " agregado con éxito.");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

