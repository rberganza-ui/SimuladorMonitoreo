    private void btnActivarDesactivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActivarDesactivarActionPerformed
        // TODO add your handling code here:
        if (sensorSeleccionado == null) return;

        if (sensorSeleccionado.isActivo()) {
            sensorSeleccionado.desactivar();
            btnActivarDesactivar.setText("Activar");
        } else {
            sensorSeleccionado.activar();
            btnActivarDesactivar.setText("Desactivar");
        }
        actualizarPanelActuadores();
    }//GEN-LAST:event_btnActivarDesactivarActionPerformed
    private void actualizarPanelActuadores() {
        
        if (sensorSeleccionado.isActivo()) {
            btnActivarDesactivar.setText("Desactivar");
        } else {
            btnActivarDesactivar.setText("Activar");
        }
        
        labelTipo.setText("Tipo: " + sensorSeleccionado.getClass().getSimpleName());
        labelValorActual.setText("Valor actual: " + sensorSeleccionado.getValorActual() + " " + sensorSeleccionado.getUnidadMedida());
        labelEstado.setText("EstadoActual: "+sensorSeleccionado.obtenerEstado(sensorSeleccionado));
        if (sensorSeleccionado instanceof SensorTemperatura st) {
            labelExtra.setVisible(true);
            labelLed.setVisible(true);
            labelBuzzer.setVisible(true);
            labelVentilador.setVisible(true);
            btndetectarBotella.setVisible(false);
           
            labelLed.setText( st.getLed().ejecutar());
            labelBuzzer.setText(st.getBuzzer().ejecutar());
            labelVentilador.setText( st.getVentilador().ejecutar());
            labelVentilador.setVisible(true);
            labelExtra.setText("Promedio: " + st.calcularPromedio() + " °C");
            campoMinutos.setVisible(false);
            btnCalcularVolumen.setVisible(false);
            labelVolumen.setVisible(false);
        } else if (sensorSeleccionado instanceof SensorNivel sn) {
            labelExtra.setVisible(true);
            labelLed.setVisible(true);
            labelBuzzer.setVisible(true);
            btndetectarBotella.setVisible(false);
            
            
            labelLed.setText(sn.getLed().ejecutar());
            labelBuzzer.setText( sn.getBuzzer().ejecutar());
            labelVentilador.setVisible(false);
            labelExtra.setText("Nivel: " + sn.calcularPorcentaje() + "%");
            campoMinutos.setVisible(false);
            btnCalcularVolumen.setVisible(false);
            labelVolumen.setVisible(false);
        } else if (sensorSeleccionado instanceof SensorPresion sp) {
            labelExtra.setVisible(true);
            labelLed.setVisible(true);
            labelBuzzer.setVisible(true);
            btndetectarBotella.setVisible(false);
            
            
            labelLed.setText( sp.getLed().ejecutar());
            labelBuzzer.setText(sp.getBuzzer().ejecutar());
            labelVentilador.setVisible(false);
            labelExtra.setText("");
        }
        else if(sensorSeleccionado instanceof SensorProximidad spro){
            labelExtra.setVisible(true);
            labelLed.setVisible(true);
            labelBuzzer.setVisible(true);
            btndetectarBotella.setVisible(true);
            
            labelExtra.setText("Botellas detectadas: " + spro.getCantidadBotellasDetectadas());
            campoMinutos.setVisible(false);
            btnCalcularVolumen.setVisible(false);
            labelVolumen.setVisible(false);
        }
        else if(sensorSeleccionado instanceof SensorCaudal sc){
            labelExtra.setVisible(true);
            labelLed.setVisible(true);
            labelBuzzer.setVisible(true);
            btndetectarBotella.setVisible(false);
            
            labelExtra.setVisible(false);
            campoMinutos.setVisible(true);
            btnCalcularVolumen.setVisible(true);
            labelVolumen.setVisible(true);
        }
}
